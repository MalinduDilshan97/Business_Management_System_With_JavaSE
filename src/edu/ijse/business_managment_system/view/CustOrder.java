    private void lblMakeOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblMakeOrderMouseClicked
        DefaultTableModel dtm=(DefaultTableModel)tblOrder.getModel();
        String orderId=txtOrderID.getText();
        String cid=(String) cmbCID.getSelectedItem();
        String date = txtDate.getText();
        
        CustomerOrderDTO customerOrderDTO = new CustomerOrderDTO(orderId,cid,date);
        
        ArrayList<CustomerOrderDetailDTO> allCustomerOrderDetails = new ArrayList<>();
        
        for(int i=0;i<dtm.getRowCount();i++){
            CustomerOrderDetailDTO CustomerOrderDetail = new CustomerOrderDetailDTO(
                    txtOrderID.getText(),
                    dtm.getValueAt(i, 0).toString(),
                    dtm.getValueAt(i, 1).toString(),
                    Double.parseDouble(dtm.getValueAt(i, 3).toString()),
                    Double.parseDouble(dtm.getValueAt(i, 4).toString()),
                    Integer.parseInt(dtm.getValueAt(i, 5).toString()));
                    
            allCustomerOrderDetails.add(CustomerOrderDetail);
        }
        
        try {
            boolean result = ctrlCustomerOrder.add(customerOrderDTO, allCustomerOrderDetails);
            if (result){
                JOptionPane.showMessageDialog(this, "Order has been successfully placed","Success",JOptionPane.INFORMATION_MESSAGE);
                
                pnlBackPayment.setVisible(true);
                txtTotal.setText(""+totalAmount);
                 
            }else{
                JOptionPane.showMessageDialog(this, "Failed to place the Order", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Failed to place the Order", "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(CustOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
    }//GEN-LAST:event_lblMakeOrderMouseClicked
