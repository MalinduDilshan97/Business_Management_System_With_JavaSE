    private void loadBatchID(String code) {
        
         String bid = null;
         String icode = null;
         int qty = 0;
        try {
            ArrayList<BatchDTO> allbatch=ctrlBatch.getAll();
            if (allbatch == null) {
                return;
            }
            cmbBatch.removeAllItems();
            for(BatchDTO batch: allbatch){
                bid=batch.getBatchId();
                icode=batch.getiCode();
                qty=batch.getQtyOnHand();
                if(icode.matches(code)){
                    if(qty>0){
                        cmbBatch.addItem(bid);
                    }
                }
                   
               
            }
           
            
        } catch (Exception ex) {
            Logger.getLogger(SupOrder.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }

