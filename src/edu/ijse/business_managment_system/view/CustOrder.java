    private void lblCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblCartMouseClicked
      
         boolean isCheck=Check();
            if(isCheck){
        
                    String itemCode=(String)cmbICode.getSelectedItem();
                    DefaultTableModel dtm=(DefaultTableModel)tblOrder.getModel();
                    String batch=(String) cmbBatch.getSelectedItem();
                    String model=txtModel.getText();
                    double unitPrice=Double.parseDouble(txtUnit.getText());
                    double discount=Double.parseDouble(txtDiscount.getText());
                    int Qty=Integer.parseInt(txtQTY.getText());
                   


                        for(int i=0;i<dtm.getRowCount();i++){
                            String code=(String)dtm.getValueAt(i,0);
                            if(itemCode.equals(code)){
                                int qty=(int)dtm.getValueAt(i,5);	
                                Qty+=qty;
                                dtm.removeRow(i);
                                totalAmount-=qty*(unitPrice-discount);
                            }
                        }
                        double amount=(unitPrice-discount)*Qty;

                         totalAmount+=amount;
                        txtAmount.setText(""+totalAmount);


                        Object[] rowData={itemCode,batch,model,unitPrice,discount,Qty,amount};	
                        dtm.addRow(rowData);

                         txtQTY.setText("");
                         txtDiscount.setText("0");
                         
             }else{
                 JOptionPane.showMessageDialog(this, "Check Again, And Input Validate Data To Proceed. ", "Error", JOptionPane.ERROR_MESSAGE);
            } 
                
    }//GEN-LAST:event_lblCartMouseClicked

