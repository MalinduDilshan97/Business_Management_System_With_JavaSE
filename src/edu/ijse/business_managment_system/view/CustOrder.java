    private void printReport() {
       try {
           
            JasperReport compiledReport = (JasperReport) JRLoader.loadObject(CustOrder.class.getResource("/edu/ijse/business_managment_system/jasper/CustomerBill.jasper"));
            
            HashMap<String, Object> parameters = new HashMap<>();
             parameters.put("FirstName", txtFName.getText());
             parameters.put("oid", txtOrderID.getText());
             parameters.put("Total", txtTotal.getText());
             parameters.put("Payment", txtPayment.getText());
             parameters.put("Balance", txtBalance.getText());
            JasperPrint filledReport = JasperFillManager.fillReport(compiledReport,parameters, new JRTableModelDataSource(tblOrder.getModel()));
            
            MyJasperView frmJasperViewer = new MyJasperView(filledReport);
            frmJasperViewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(CustOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
    }

   
