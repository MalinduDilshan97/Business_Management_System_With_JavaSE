    private void lblCart1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblCart1MouseClicked
         
         boolean isCheck=Check();
            if(isCheck){
        
                String itemCode=(String)cmbCode.getSelectedItem();
                    DefaultTableModel dtm=(DefaultTableModel)tblOrder.getModel();
                    String brand=txtBrand.getText();
                    String model=txtModel.getText();
                    int orderQty=Integer.parseInt(txtQTY.getText());


                        for(int i=0;i<dtm.getRowCount();i++){
                            String code=(String)dtm.getValueAt(i,0);
                            if(itemCode.equals(code)){
                                int qty=(int)dtm.getValueAt(i,3);	
                                orderQty+=qty;
                                dtm.removeRow(i);	
                            }
                        }


                        Object[] rowData={itemCode,brand,model,orderQty};	
                        dtm.addRow(rowData);
                        
                        txtQTY.setText("");
					
           }else{
                 JOptionPane.showMessageDialog(this, "Check Again, And Input Validate Data To Proceed. ", "Error", JOptionPane.ERROR_MESSAGE);
            } 
    }//GEN-LAST:event_lblCart1MouseClicked

