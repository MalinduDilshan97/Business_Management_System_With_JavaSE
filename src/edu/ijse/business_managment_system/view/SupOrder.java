    private void printReport() {
         try {
           
            JasperReport compiledReport = (JasperReport) JRLoader.loadObject(SupOrder.class.getResource("/edu/ijse/business_managment_system/jasper/SupplierOrder.jasper"));
            
            HashMap<String, Object> parameters = new HashMap<>();
             parameters.put("SupplierName", txtSupName.getText());
            parameters.put("OID", txtOrderID.getText());
            JasperPrint filledReport = JasperFillManager.fillReport(compiledReport,parameters, new JRTableModelDataSource(tblOrder.getModel()));
            
            MyJasperView frmJasperViewer = new MyJasperView(filledReport);
            frmJasperViewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(SupOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
