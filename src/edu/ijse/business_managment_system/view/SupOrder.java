    private void loadSID() {
        
        String sid = null;
        try {
            ArrayList<SupplierDTO> allsupplier=ctrlSupplier.getAll();
            if (allsupplier == null) {
                return;
            }
            cmbSid.removeAllItems();
            for(SupplierDTO supplier: allsupplier){
                sid=supplier.getSid();
                cmbSid.addItem(sid);
            }
           
            
        } catch (Exception ex) {
            Logger.getLogger(SupOrder.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }

