    private void txtUIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUIDActionPerformed
       String uid=txtUID.getText();
		try{
		UserDTO user=ctrlUser.search(uid);
		if(user!=null){
			txtUserName.setText(user.getUserName());
			txtPassword.setText(user.getPassword());
		}else{
			JOptionPane.showMessageDialog(ManageUser.this,"User not found...");
		}
		}catch(SQLException e){
			JOptionPane.showMessageDialog(ManageUser.this,e.getMessage());
		}catch(ClassNotFoundException e){
                    JOptionPane.showMessageDialog(ManageUser.this,e.getMessage());
		} catch (Exception ex) {	
             Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
         }	 
    }//GEN-LAST:event_txtUIDActionPerformed

