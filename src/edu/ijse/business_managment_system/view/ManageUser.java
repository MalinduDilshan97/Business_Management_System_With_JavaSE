    private void loadUsers() {
         dtmUser.setRowCount(0);

        try {
            ArrayList<UserDTO> allUsers = ctrlUser.getAll();

            if (allUsers == null) {
                return;
            }

            for (UserDTO user : allUsers) {
                Object[] rowData = {
                    user.getUid(),
                    user.getUserName(),
                    user.getPassword()
                };
                dtmUser.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void Back() {
        this.dispose();
        tmrTime.stop();
    }

    private void DeleteUser() {
        String uid=txtUID.getText();
        try{
	boolean isDeleted=ctrlUser.delete(uid);
	if(isDeleted){
            loadUsers();
            Clear();
            JOptionPane.showMessageDialog(ManageUser.this," User Deleted...");
	}else{
            JOptionPane.showMessageDialog(ManageUser.this,"User not found...");
	}
	}catch(SQLException e){
		System.out.println(e.getMessage());
	}catch(ClassNotFoundException e){
		System.out.println(e.getMessage());
	} catch (Exception ex) {		
             Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
         }	
    }

    private void UpdateUser() {
        
        boolean isCheck=Check();
        if(isCheck){
        
                String uid=txtUID.getText();
                String username = txtUserName.getText();
                String password = txtPassword.getText();
        
                UserDTO userDTO = new UserDTO(uid,username,password);
                try{
                    boolean isUpdate=ctrlUser.update(userDTO);
                if(isUpdate){
                    loadUsers();
                    Error();
                    JOptionPane.showMessageDialog(ManageUser.this,"User Update Success...");
                }else{
                    JOptionPane.showMessageDialog(ManageUser.this,"User Update Fail");
                }
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(ManageUser.this,e.getMessage());
                }catch(ClassNotFoundException e){
                    JOptionPane.showMessageDialog(ManageUser.this,e.getMessage());
                } catch (Exception ex) {		
                     Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
                }
         }else{
             lblUserError.setVisible(true);
             JOptionPane.showMessageDialog(this, "Check Again, And Input Validate Data To Proceed", "Error", JOptionPane.ERROR_MESSAGE);
         }
    }

    private void AddUser() {
        boolean isCheck=Check();
        if(isCheck){
        
                String uid=txtUID.getText();
                String username = txtUserName.getText();
                String password = txtPassword.getText();
       
                 UserDTO userDTO = new UserDTO(uid,username,password);
     
                try{
                    boolean isAdded=ctrlUser.add(userDTO);
                if(isAdded){
                    Error();
                    loadUsers();
                    JOptionPane.showMessageDialog(ManageUser.this,"User Added Successfully");
                }else{
                   JOptionPane.showMessageDialog(this, "User Added Fail", "Error", JOptionPane.ERROR_MESSAGE); 
                }
                }catch(SQLException e){
                    String errorMsg;
                switch (e.getErrorCode()){
                    case 1062:
                        errorMsg = "Duplicate entry has been found. Please enter valid User Id to proceed.";
                        break;
                    default:
                        errorMsg = "User has been failed to add";
                        break;
                }
                JOptionPane.showMessageDialog(this, errorMsg, "Error", JOptionPane.ERROR_MESSAGE);
                }catch(ClassNotFoundException e){
                    JOptionPane.showMessageDialog(ManageUser.this,e);
                } catch (Exception ex) {
                     Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
                }
                
        }else{
            lblUserError.setVisible(true);
        }
         
    }

    private void loadKeyListener() {
         KeyboardFocusManager manger = KeyboardFocusManager.getCurrentKeyboardFocusManager();
        manger.addKeyEventPostProcessor(new KeyEventPostProcessor() {

            @Override
            public boolean postProcessKeyEvent(KeyEvent e) {
                if(e.getID()== KeyEvent.KEY_PRESSED){
                    switch(e.getKeyCode()){
                        case KeyEvent.VK_CONTROL:
                            AddUser();break;
                        case KeyEvent.VK_ALT:
                            UpdateUser();break;
                         case KeyEvent.VK_DELETE:
                            DeleteUser();break;
                        case KeyEvent.VK_ESCAPE:
                            Back();break;
                        
                    }
                }
                return false;
            }
        });
    }

   

