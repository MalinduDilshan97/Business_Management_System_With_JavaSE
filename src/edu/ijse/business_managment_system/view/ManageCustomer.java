    private void loadKeyListener() {
        KeyboardFocusManager manger = KeyboardFocusManager.getCurrentKeyboardFocusManager();
        manger.addKeyEventPostProcessor(new KeyEventPostProcessor() {

            @Override
            public boolean postProcessKeyEvent(KeyEvent e) {
                if(e.getID()== KeyEvent.KEY_PRESSED){
                    switch(e.getKeyCode()){
                        case KeyEvent.VK_CONTROL:
                            AddCustomer();break;
                        case KeyEvent.VK_ALT:
                            UpdateCustomer();break;
                         case KeyEvent.VK_DELETE:
                            DeleteCustomer();break;
                        case KeyEvent.VK_HOME:
                            ViewAllCustomer();break;
                        case KeyEvent.VK_ESCAPE:
                            Back();break;
                        
                    }
                }
                return false;
            }
        });
    }

    private void AddCustomer() {
        
          boolean isCheck=Check();
        if(isCheck){
        
            String cid=txtCID.getText();
            String first = txtFirst.getText();
            String last = txtLast.getText();
            String nic = txtNic.getText();
            String contact = txtContact.getText();
            String email = txtEmail.getText();
            String address = txtAddress.getText();

            CustomerDTO customerDTO = new CustomerDTO(cid,first,last,nic,contact,email,address);
            try{
                boolean isAdded=ctrlCustomer.add(customerDTO);
            if(isAdded){
                JOptionPane.showMessageDialog(ManageCustomer.this,"Customer Added Successfully");
            }
            } catch (SQLException ex) {
            String errorMsg;

            ImageIcon icon = new ImageIcon(this.getClass().getResource("/edu/ijse/business_managment_system/view/pics/error_icon.png"));

            switch (ex.getErrorCode()) {
                case 1062:
                    errorMsg = "Duplicate entry has been found. Please enter valid Customer Id to proceed.";
                    break;
                default:
                    errorMsg = "Customer has been failed to add";
                    break;
                }
            }catch(ClassNotFoundException e){
                JOptionPane.showMessageDialog(ManageCustomer.this,e);
            } catch (Exception ex) {
                  Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
             JOptionPane.showMessageDialog(this, "Check Again, And Input Validate Data To Proceed", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
             
         
    }

    private void UpdateCustomer() {
        
         boolean isCheck=Check();
        if(isCheck){
            
                String cid=txtCID.getText();
                String first = txtFirst.getText();
                String last = txtLast.getText();
                String nic = txtNic.getText();
                String contact = txtContact.getText();
                String email = txtEmail.getText();
                String address = txtAddress.getText();
                CustomerDTO customerDTO=new CustomerDTO(cid,first,last,nic,contact,email,address);
                try{
                    boolean isUpdate=ctrlCustomer.update(customerDTO);
                if(isUpdate){
                    JOptionPane.showMessageDialog(ManageCustomer.this," Customer Update Success...");
                }else{
                    JOptionPane.showMessageDialog(ManageCustomer.this,"Customer Update Fail");
                }
                }catch(SQLException e){
                    JOptionPane.showMessageDialog(ManageCustomer.this,e.getMessage());
                }catch(ClassNotFoundException e){
                    JOptionPane.showMessageDialog(ManageCustomer.this,e.getMessage());
                } catch (Exception ex) {				
                      Logger.getLogger(ManageCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
        }else{
             JOptionPane.showMessageDialog(this, "Check Again, And Input Validate Data To Proceed. ", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

